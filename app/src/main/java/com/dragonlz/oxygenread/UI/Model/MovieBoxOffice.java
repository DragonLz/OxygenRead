public class MovieBoxOffice {

    private String movieName;
    private String movieTheNumber;
    private String movieComment;
    private String movieMoney;

    public String getMovieName() {
        return movieName;
    }

    public void setMovieName(String movieName) {
        this.movieName = movieName;
    }

    public String getMovieTheNumber() {
        return movieTheNumber;
    }

    public void setMovieTheNumber(String movieTheNumber) {
        this.movieTheNumber = movieTheNumber;
    }

    public String getMovieComment() {
        return movieComment;
    }

    public void setMovieComment(String movieComment) {
        this.movieComment = movieComment;
    }

    public String getMovieMoney() {
        return movieMoney;
    }

    public void setMovieMoney(String movieMoney) {
        this.movieMoney = movieMoney;
    }
}
